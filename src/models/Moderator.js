const mongoose = require('mongoose');

const ModeratorSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true
  },
  lastName: {
    type: String,
    required: true
  },
  username: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  }
});

const Moderator = mongoose.model('moderators', ModeratorSchema);
module.exports = Moderator;
